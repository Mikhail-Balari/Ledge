def compile_to_c(source: str) -> str:
    """Compile Ledge source to C99 source code."""
    from ..lexer import Lexer
    from ..parser import Parser
    tokens = Lexer(source).tokenize()
    program = Parser(tokens).parse()
    gen = CCodegen()
    return gen.compile(program)


def compile_to_native(source: str, output_path: str = "a.out",
                      optimize: bool = True) -> str:
    """
    Compile Ledge source to a native binary using gcc.

    Requirements: gcc (standard on Linux/macOS)

    Returns: path to the compiled binary
    """
    import subprocess, tempfile, os, shutil

    gcc = shutil.which('gcc') or shutil.which('cc')
    if not gcc:
        raise RuntimeError(
            "Native compilation requires gcc. Install it:\n"
            "  Ubuntu/Debian: sudo apt install gcc\n"
            "  macOS:         xcode-select --install"
        )

    c_source = compile_to_c(source)

    with tempfile.NamedTemporaryFile(suffix='.c', mode='w', delete=False) as f:
        f.write(c_source)
        c_path = f.name

    try:
        opt_flags = ['-O3', '-march=native'] if optimize else ['-O0']
        cmd = [gcc] + opt_flags + ['-o', output_path, c_path, '-lm']
        result = subprocess.run(cmd, capture_output=True, text=True)
        if result.returncode != 0:
            raise RuntimeError(f"GCC compilation failed:\n{result.stderr}")
        os.chmod(output_path, 0o755)
        return os.path.abspath(output_path)
    finally:
        os.unlink(c_path)
