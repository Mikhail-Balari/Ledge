"""
Ledge → C Code Generator
=========================
Compiles Ledge programs to portable C99 that gcc can compile to native binaries.

This is the primary native compiler backend for Ledge v1.2.

Architecture:
  Ledge source
    → AST (parser.py)
    → C99 source (ccodegen.py)
    → Native binary (via gcc/clang)

Type mapping:
  number  → double
  text    → LedgeStr* (reference-counted string)
  truth   → int (0/1)
  nothing → special NOTHING sentinel
  list    → LedgeList* (dynamic array)
  map     → LedgeMap* (hash map)
  Uncertain[T] → LedgeUncertain* {value, confidence, source}
  function → function pointer + closure struct

Reference counting GC:
  Every heap-allocated value is reference counted.
  ldg_incref / ldg_decref manage lifetime.
  No cycles (by design — Ledge has no circular references in practice).

Performance target:
  Numeric compute: 10-50x faster than CPython
  String heavy: 3-5x faster
  Mixed AI workload: 2-5x faster
"""

