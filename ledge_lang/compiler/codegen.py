def compile_to_ir(source: str) -> str:
    """
    Compile Ledge source code to LLVM IR text.
    
    Returns a string containing valid LLVM IR that can be:
    - Saved as .ll file and compiled with clang/llc
    - Passed to llvmlite for JIT compilation
    - Used as input to emcc for WASM compilation
    
    Example:
        ir = compile_to_ir("show 42")
        with open("out.ll", "w") as f: f.write(ir)
        # Then: clang out.ll -o out && ./out
    """
    from ..lexer import Lexer
    from ..parser import Parser
    tokens = Lexer(source).tokenize()
    program = Parser(tokens).parse()
    codegen = LLVMCodegen()
    return codegen.compile(program)
